    private void chatMsgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chatMsgActionPerformed
        
            //입력창 값 가져와서 String에 저장
            String myMsg = chatMsg.getText();
            String resMsg="";
            if(myMsg.contains("등록")) {
            	resMsg =  "등록";
            }else {
            	resMsg = myMsg;
            }
            System.out.println("입력값:"+myMsg);
            String res = ref.message(resMsg);
            //TextArea창에 입력한 값을 간단하게 출력을 해본다.
            chatMessage.append("\t\t테스형:"+myMsg+"\n");
            chatMessage.append("매니저:"+res+"\n");
            chatMsg.setText("");
   
    }//GEN-LAST:event_chatMsgActionPerformed

